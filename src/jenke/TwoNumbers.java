package jenke;

public class TwoNumbers
{

    private int _number1;
    private int _number2;

    public TwoNumbers(int number1, int number2)
    {
        _number1 = number1;
        _number2 = number2;
    }

    public int getNumber1()
    {
        return _number1;
    }

    public int getNumber2()
    {
        return _number2;
    }

}
